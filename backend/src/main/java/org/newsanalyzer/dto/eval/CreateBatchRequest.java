package org.newsanalyzer.dto.eval;

import jakarta.validation.Valid;
import jakarta.validation.constraints.NotBlank;
import jakarta.validation.constraints.NotEmpty;
import jakarta.validation.constraints.NotNull;
import lombok.AllArgsConstructor;
import lombok.Builder;
import lombok.Data;
import lombok.NoArgsConstructor;

import java.util.List;
import java.util.Map;
import java.util.UUID;

/**
 * Request body for POST /api/eval/datasets/batches.
 * Sent by the reasoning-service after a batch generation run completes.
 *
 * This is the cross-service contract — the reasoning-service serializes
 * its BatchResult + ArticleTestCases into this shape.
 */
@Data
@NoArgsConstructor
@AllArgsConstructor
@Builder
public class CreateBatchRequest {

    /** Batch ID generated by the reasoning-service orchestrator. */
    @NotNull
    private UUID batchId;

    /** Government branch filter used (null = all branches). */
    private String branch;

    /** Model used for article generation (e.g., "claude-sonnet-4-20250514"). */
    @NotBlank
    private String modelUsed;

    /** Full BatchConfig as JSON. */
    @NotNull
    private Map<String, Object> configJson;

    /** Total articles generated in this batch. */
    private int articlesCount;

    /** Number of faithful (unperturbed) articles. */
    private int faithfulCount;

    /** Number of perturbed articles. */
    private int perturbedCount;

    /** Total API tokens used. */
    private int totalTokens;

    /** Batch duration in seconds. */
    private Double durationSeconds;

    /** Any errors encountered during generation. */
    private List<String> errors;

    /** The generated articles with ground-truth labels. */
    @NotEmpty
    @Valid
    private List<ArticleEntry> articles;

    /**
     * A single article within the batch request.
     */
    @Data
    @NoArgsConstructor
    @AllArgsConstructor
    @Builder
    public static class ArticleEntry {

        @NotBlank
        private String articleText;

        @NotBlank
        private String articleType;

        @NotNull
        private Boolean isFaithful;

        private String perturbationType;

        @NotBlank
        private String difficulty;

        /** FactSet serialized as JSON map. */
        @NotNull
        private Map<String, Object> sourceFacts;

        /** Ground truth: changed_facts + expected_findings. */
        @NotNull
        private Map<String, Object> groundTruth;

        @NotBlank
        private String modelUsed;

        private int tokensUsed;
    }
}
